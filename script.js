renderAnime(animeData);

const searchInput = document.getElementById("searchInput");
const genreFilter = document.getElementById("genreFilter");

searchInput.addEventListener("input", filterAnime);
genreFilter.addEventListener("change", filterAnime);

function filterAnime() {
  const searchValue = searchInput.value.toLowerCase().trim();

  if (searchValue === "") {
    searchModal.style.display = "none";
    return;
  }

  const filtered = animeData.filter(anime =>
    anime.title.toLowerCase().includes(searchValue)
  );

  searchResults.innerHTML = "";

  filtered.forEach(anime => {
    const card = document.createElement("div");
    card.classList.add("anime-card");

    card.innerHTML = `
      <img src="${anime.image}" />
      <div class="anime-info">
        <h3>${anime.title}</h3>
      </div>
    `;

    card.addEventListener("click", () => {
      searchModal.style.display = "none";
      openModal(anime);
    });

    searchResults.appendChild(card);
  });

  searchModal.style.display = "block";
}
document.addEventListener("click", (e) => {
  if (!e.target.closest(".search-wrapper")) {
    searchModal.style.display = "none";
  }
});



// Open Modal
function openModal(anime, autoPlay = false) {
  modal.style.display = "flex";
  document.body.style.overflow = "hidden";

  modalTitle.textContent = anime.title;
  modalImage.src = anime.image;
  modalDescription.textContent = anime.description;

  episodeList.innerHTML = "";
  videoPlayer.src = "";

  animeEpisodes[anime.id].forEach((episodeSrc, index) => {
    const btn = document.createElement("button");
    btn.classList.add("episode-btn");
    btn.textContent = `Episode ${index + 1}`;

    btn.addEventListener("click", () => {
      videoPlayer.src = episodeSrc;
    });

    episodeList.appendChild(btn);
  });

  if (autoPlay) {
    videoPlayer.src = animeEpisodes[anime.id][0];
  }
}

